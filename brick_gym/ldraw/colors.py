def halton_sequence(index, base):
    f = 1
    r = 0
    
    while index > 0:
        f = f/base
        r = r + f * (index % base)
        index = index // base
    
    return r

def index_to_color(index):
    index += 1
    r = halton_sequence(index, 2)
    #r = round(r * 255) / 255
    g = halton_sequence(index, 3)
    #g = round(g * 255) / 255
    b = halton_sequence(index, 5)
    #b = round(b * 255) / 255
    
    return r,g,b

